class Persona:
    def __init__(self, nombre, domicilio, telefono, mail):
        self.nombre = nombre
        self.domicilio = domicilio
        self.telefono = telefono
        self.mail = mail
        
        def imprimir(self):
            print(self.nombre)
            print(self.domicilio)
            print(self.telefono)
            print(self.mail)